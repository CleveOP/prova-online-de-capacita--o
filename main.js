// Modifique a função verificarResposta para bloquear os botões após resposta
async function verificarResposta(indiceResposta) {
    bloquearRespostas(); // Bloqueia os botões após clicar

    const resposta = window.respostasEmbaralhadas[indiceResposta];
    gifAnimacao.innerHTML = "";

    if (resposta.correto) {
        mensagemElemento.textContent = "🎉 Parabéns, você acertou!";
        mensagemElemento.style.color = "green";

        // Atualiza a pontuação do jogador atual
        if (jogadorAtual === 1) {
            pontuacaoJogador1 += 10;
        } else {
            pontuacaoJogador2 += 10;
        }

        // Busca e exibe o GIF de acerto
        const gifUrl = await buscarGif("success", gifsUsadosAcerto);
        if (gifUrl) gifAnimacao.innerHTML = `<img src="${gifUrl}" alt="Acerto">`;
    } else {
        mensagemElemento.textContent = "❌ Ops, você errou!";
        mensagemElemento.style.color = "red";

        // Atualiza a pontuação do jogador atual
        if (jogadorAtual === 1) {
            pontuacaoJogador1 -= 5;
        } else {
            pontuacaoJogador2 -= 5;
        }

        // Busca e exibe o GIF de erro
        const gifUrl = await buscarGif("fail", gifsUsadosErro);
        if (gifUrl) gifAnimacao.innerHTML = `<img src="${gifUrl}" alt="Erro">`;
    }

    // Atualiza a exibição das pontuações
    atualizarPontuacao();

    // Alterna para o próximo jogador
    jogadorAtual = jogadorAtual === 1 ? 2 : 1;

    gifAnimacao.style.display = "block";

    setTimeout(() => {
        gifAnimacao.style.display = "none";
        indiceAtual++;
        if (indiceAtual < perguntas.length) {
            carregarPergunta();
        } else {
            finalizarJogo();
        }
    }, 2000);
}

