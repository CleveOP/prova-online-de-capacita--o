// Função para gerar URLs de avatares aleatórios
function gerarAvataresAleatorios(quantidade) {
    const avatares = [];
    const idsUsados = new Set(); // Para evitar repetição de IDs

    while (avatares.length < quantidade) {
        const idAleatorio = Math.floor(Math.random() * 70) + 1; // IDs de 1 a 70
        if (!idsUsados.has(idAleatorio)) {
            idsUsados.add(idAleatorio);
            avatares.push(`https://i.pravatar.cc/100?img=${idAleatorio}`);
        }
    }

    return avatares;
}

// Função para renderizar os avatares
function renderizarAvatares() {
    const avataresAleatorios = gerarAvataresAleatorios(10); // Gera 10 avatares únicos

    // Limpa o contêiner de avatares
    gradeAvatares.innerHTML = "";

    // Adiciona os avatares ao contêiner
    avataresAleatorios.forEach((url, index) => {
        const avatarDiv = document.createElement("div");
        avatarDiv.classList.add("avatar");
        avatarDiv.setAttribute("data-avatar", url);
        avatarDiv.innerHTML = `<img src="${url}" alt="Avatar ${index + 1}">`;
        gradeAvatares.appendChild(avatarDiv);
    });

    // Atualiza os eventos de clique nos avatares
    atualizarEventosAvatares();
}

// Função para buscar uma imagem aleatória da API do Unsplash
async function definirImagemDeFundo(termo) {
    const apiKey = "3XXMNv1PEIXPmIuLtYxAAHIb0so5RzLRby20iX8NFTg"; // Substitua pela sua Access Key do Unsplash
    const url = `https://api.unsplash.com/photos/random?query=${termo}&orientation=landscape&client_id=${apiKey}`;

    try {
        const resposta = await fetch(url);
        const dados = await resposta.json();

        if (dados && dados.urls && dados.urls.full) {
            const novaImagem = new Image(); // Cria um objeto de imagem para pré-carregar
            novaImagem.src = dados.urls.full;

            // Aguarda o carregamento da imagem antes de aplicá-la
            novaImagem.onload = () => {
                document.body.style.backgroundImage = `url(${dados.urls.full})`;
                document.body.style.backgroundSize = "cover";
                document.body.style.backgroundPosition = "center";
            };
        } else {
            console.warn("Nenhuma imagem encontrada para o termo:", termo);
        }
    } catch (erro) {
        console.error("Erro ao buscar imagem de fundo:", erro);
    }
}

